package pkg1023;

public class Saram {
	private String name;
	private String address;

	public Saram(String name, String address) {
		this.name = name; // 주문자 이름
		this.address = address;// 배송지
	}

	public String getName() {
		return name;
	}

	public String getAddress() {
		return address;
	}
	
	

}
